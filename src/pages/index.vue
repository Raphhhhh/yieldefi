<template>
  <vs-row>
    <vs-col
      vs-type="flex"
      vs-justify="center"
      vs-align="center"
      w="12"
      :class="$style.container"
    >
      <template v-if="!address">
        <vs-button gradient size="xl" :class="$style.connect" @click="connect">
          <i class="bx bx-rocket"></i><span>Connect to wallet</span>
        </vs-button>
      </template>
      <template v-else-if="projects && projects.length > 0">
        <vs-row
          v-for="project in projects"
          :key="project"
          :class="$style.project"
        >
          <vs-col vs-type="flex" vs-justify="center" vs-align="center" w="12">
            <project :project="project" />
          </vs-col>
        </vs-row>
      </template>
    </vs-col>
  </vs-row>
</template>

<script>
import { mapState, mapGetters } from 'vuex'
import Project from '~/components/project'
export default {
  components: {
    Project,
  },
  computed: {
    ...mapState({
      address: (state) => state.ethers.address,
    }),
    ...mapGetters({
      projects: 'user/getProjects',
    }),
  },
  watch: {
    address(a) {
      if (a) {
        this.$store.dispatch('user/fetch')
      }
    },
  },
  methods: {
    async connect() {
      await this.$store.dispatch('ethers/init')
    },
    disconnect() {
      this.$store.dispatch('ethers/disconnect')
    },
  },
}
</script>

<style lang="stylus" module>
.container
  text-align center
  .project
    margin-bottom 20px
</style>
